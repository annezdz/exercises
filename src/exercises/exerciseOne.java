package exercises;

public class exerciseOne {
    public static void main(String[] args) {
            int x = 1;
            while(x < 10){
                if(x > 3){
                    System.out.println("big x");
                }
                x ++;
            }

        int y = 5;
        while(y > 1){
            y --;
            if(y < 3){
                System.out.println("small y");
            }
        }
        int z = 5;
        while(z > 1){
            z--;
            if(z < 3){
                System.out.println("small z");
            }
        }
    }
}
